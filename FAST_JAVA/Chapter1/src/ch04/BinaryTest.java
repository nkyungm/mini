package ch04;

public class BinaryTest {

	public static void main(String[] args) {
		int num =10;
		int bNum=0B1010; //(0B)2진수
		int oNum=012; //(0)8진수
		int xNum=0XA; //(0X)16진수
		
		System.out.println(num);
		System.out.println(bNum);
		System.out.println(oNum);
		System.out.println(xNum);
	}

}
